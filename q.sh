FROM T_DGAP_WS_ERROR_LOG T
LEFT JOIN T_DGAP_RESOURCE_DIRECTORY TD ON T.RESOURCE_DIRECTORY_ID = TD.ID
WHERE T.DEL_FLAG = 'N'
ORDER BY T.UPDATE_TIME DESC
) M )
